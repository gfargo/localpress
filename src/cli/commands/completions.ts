// -- Bash completions ---------------------------------------------------------

function generateBash(): string {
  return `# localpress bash completion
# Generated by: localpress completions bash
# Add to ~/.bashrc or ~/.bash_profile:
#   eval "$(localpress completions bash)"
# Or:
#   localpress completions bash >> ~/.bashrc

_localpress() {
  local cur prev commands global_opts
  COMPREPLY=()
  cur="\${COMP_WORDS[COMP_CWORD]}"
  prev="\${COMP_WORDS[COMP_CWORD-1]}"

  commands="${COMMANDS.join(' ')}"
  global_opts="${GLOBAL_OPTIONS.join(' ')}"

  # Complete commands at position 1
  if [[ \${COMP_CWORD} -eq 1 ]]; then
    COMPREPLY=( $(compgen -W "\${commands} \${global_opts}" -- "\${cur}") )
    return 0
  fi

  # Complete global options and subcommand-specific options
  local cmd="\${COMP_WORDS[1]}"
  case "\${cmd}" in
    init)
      COMPREPLY=( $(compgen -W "--site --help" -- "\${cur}") )
      ;;
    sites)
      COMPREPLY=( $(compgen -W "list add remove use --help" -- "\${cur}") )
      ;;
    doctor)
      COMPREPLY=( $(compgen -W "--all-sites --plugins --fix --help" -- "\${cur}") )
      ;;
    config)
      COMPREPLY=( $(compgen -W "get set list set-profile get-profile list-profiles remove-profile --help" -- "\${cur}") )
      ;;
    list)
      COMPREPLY=( $(compgen -W "-i --interactive --unoptimized --type --sort --order --limit --page --help" -- "\${cur}") )
      ;;
    show)
      COMPREPLY=( $(compgen -W "--help" -- "\${cur}") )
      ;;
    stats)
      COMPREPLY=( $(compgen -W "--all-sites --help" -- "\${cur}") )
      ;;
    caption)
      COMPREPLY=( $(compgen -W "--missing-alt --model --prompt --list-models --help" -- "\${cur}") )
      ;;
    audit)
      COMPREPLY=( $(compgen -W "--unoptimized --large --missing-alt --display-size --duplicates --broken-refs --orphans --all --threshold --help" -- "\${cur}") )
      ;;
    optimize)
      COMPREPLY=( $(compgen -W "--all --unoptimized --quality --format --max-width --max-height --encoder --strip-metadata --profile --keep-original --preview --help" -- "\${cur}") )
      ;;
    convert)
      COMPREPLY=( $(compgen -W "--format --quality --encoder --keep-original --help" -- "\${cur}") )
      ;;
    resize)
      COMPREPLY=( $(compgen -W "--max-width --max-height --keep-original --help" -- "\${cur}") )
      ;;
    remove-bg)
      COMPREPLY=( $(compgen -W "--all --unoptimized --model --bg --trim --list-models --keep-original --rembg --rembg-model --preview --help" -- "\${cur}") )
      ;;
    edit)
      COMPREPLY=( $(compgen -W "--with --no-watch --keep-file --to --help" -- "\${cur}") )
      ;;
    pull)
      COMPREPLY=( $(compgen -W "--to --help" -- "\${cur}") )
      ;;
    push)
      COMPREPLY=( $(compgen -W "--title --alt --caption --description --post-id --help" -- "\${cur}") )
      ;;
    references)
      COMPREPLY=( $(compgen -W "--scope --update-to --help" -- "\${cur}") )
      ;;
    update)
      COMPREPLY=( $(compgen -W "--check --help" -- "\${cur}") )
      ;;
    completions)
      COMPREPLY=( $(compgen -W "bash zsh fish" -- "\${cur}") )
      ;;
    *)
      COMPREPLY=( $(compgen -W "\${global_opts}" -- "\${cur}") )
      ;;
  esac

  return 0
}

complete -F _localpress localpress
`;
}

