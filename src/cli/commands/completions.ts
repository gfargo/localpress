function fishCommand(name: string, desc: string): string[] {
  return [`complete -c localpress -n "__fish_use_subcommand" -a "${name}" -d "${desc}"`];
}

function fishSubOpt(cmd: string, opt: string, desc: string, extra = ''): string[] {
  const flag = opt.startsWith('--') ? `-l ${opt.slice(2)}` : `-s ${opt.slice(1)}`;
  const extraStr = extra ? ` ${extra}` : '';
  return [
    `complete -c localpress -n "__fish_seen_subcommand_from ${cmd}" ${flag} -d "${desc}"${extraStr}`,
  ];
}
